import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { ICitizensMeetings } from '@/shared/model/citizens-meetings.model';

const baseApiUrl = 'api/citizens-meetings';

export default class CitizensMeetingsService {
  public find(id: number): Promise<ICitizensMeetings> {
    return new Promise<ICitizensMeetings>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ICitizensMeetings): Promise<ICitizensMeetings> {
    return new Promise<ICitizensMeetings>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ICitizensMeetings): Promise<ICitizensMeetings> {
    return new Promise<ICitizensMeetings>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: ICitizensMeetings): Promise<ICitizensMeetings> {
    return new Promise<ICitizensMeetings>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
